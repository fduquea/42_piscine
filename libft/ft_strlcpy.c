int	ft_strlcpy(char *dest, const char *src, int n)
{
	int i;
	int l;
	
	i = 0;
	l = 0;
	while (src[l] != '\0')
		l++;
	if (n != 0)
	{
		while (src[i] != '\0' && i < (n - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (l);
}